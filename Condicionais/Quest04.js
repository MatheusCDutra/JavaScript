//4. Faça um algoritmo que leia dois valores inteiros A e B se os valores forem iguais deverá se somar os dois, caso contrário
//multiplique A por B.

let a = Number(prompt('Digite um número: '))
let b = Number(prompt('Digite um número: '))

if (a == b) {
  console.log(a + b)
}
else {
  console.log(a * b)
}
